import {
  IndexOfferFormulaComponent,
  IndexPricingFormData,
  SpecialOfferMetadataResponseData,
} from '@modules/Dashboard/SpecialOffers/Api/types.schema'
import { FormulaDiff } from '@modules/Dashboard/SpecialOffers/Components/CreateNew/Components/ConfigureIndexPrice/Components/FormulaDiff'
import { IndexOfferComponentsColumnDefs } from '@modules/Dashboard/SpecialOffers/Components/CreateNew/Components/ConfigureIndexPrice/Components/IndexOfferComponentsColumnDefs'
import { blankFormulaComponentRow } from '@modules/Dashboard/SpecialOffers/Components/CreateNew/Components/ConfigureIndexPrice/Utils/Constants'
import {
  generateIndexOfferDisplayName,
  validateFormulaComponents,
} from '@modules/Dashboard/SpecialOffers/Components/CreateNew/Components/ConfigureIndexPrice/Utils/IndexFormHelpers'
import {
  mapIndexMetadataToSelectorMetadata,
  mapTemplateToComponents,
  mapToFormulaMetadata,
} from '@modules/Dashboard/SpecialOffers/Components/CreateNew/Components/ConfigureIndexPrice/Utils/TemplateHelpers'
import { FormulaTemplateDetails } from '@modules/FormulaTemplates/Api/types.schema'
import { FormulaComponentsGrid } from '@modules/FormulaTemplates/Components/CreateEditDrawer/Components/FormulaComponentsGrid'
